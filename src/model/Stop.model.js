export class StopModel {
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Attributes /////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    id: number;

    display_name: string;

    coordinates: number[];

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    constructor(stop) {
        const { id, address, coordinates } = stop;

        this.id = id;
        this.display_name = address;
        this.coordinates = [
            Number(coordinates.latitude),
            Number(coordinates.longitude)
        ];
    }
}