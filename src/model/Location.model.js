export class LocationModel {
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Attributes /////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    place_id: number;

    display_name: string;

    coordinates: number[];

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    constructor(location) {
        const { place_id, display_name, lat, lon } = location;

        this.place_id = place_id;
        this.display_name = display_name;
        this.coordinates = [
            Number(lat),
            Number(lon)
        ];
    }
}